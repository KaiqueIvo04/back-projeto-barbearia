Cypress.Commands.add('getToken', (email, pass) => {

    cy.request({
        method: 'POST',
        url: `http://localhost:8081/v1/auth/`,
        body: {
            "email": email,
            "password": pass
        }

    }).then(token => {
        expect(token.body.access_token).to.not.be.empty;
        return token;
    })
});